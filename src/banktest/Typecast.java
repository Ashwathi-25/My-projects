package banktest;

public class Typecast {
	public static void main(String[] args) {
		
		int a = 5;
		double b = a;//implicit casting
		
		System.out.println(a);
		System.out.println(b);
		
		double c = 11.5;
		int d = (int) c;
		
		System.out.println(c); 
		System.out.println(d); 
		
		
		
		
	}

}
